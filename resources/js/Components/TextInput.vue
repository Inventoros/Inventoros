<script setup>
import { onMounted, ref } from 'vue';

const model = defineModel({
    type: String,
    required: true,
});

const input = ref(null);

onMounted(() => {
    if (input.value.hasAttribute('autofocus')) {
        input.value.focus();
    }
});

defineExpose({ focus: () => input.value.focus() });
</script>

<template>
    <input
        class="rounded-md border-border-subtle bg-surface-canvas text-text-primary shadow-sm focus:border-brand focus:ring-brand"
        v-model="model"
        ref="input"
    />
</template>
